package com.psylife.entity;

import java.util.Date;

/**
 * 轮胎轨迹
 * @author xu
 *
 */
public class TyreHistory {
	
	/**
	 * 事件类型,入库
	 */
	public static final String ACTION_TYPE_INPUT = "入库";
	
	/**
	 * 事件类型,装载
	 */
	public static final String ACTION_TYPE_UP = "装载";
	
	/**
	 * 事件类型,卸下
	 */
	public static final String ACTION_TYPE_DOWN = "卸下";
	
	/**
	 * 事件类型,换胎
	 */
	public static final String ACTION_TYPE_EXCHANGE = "换胎";
	
	/**
	 * 事件类型,修补
	 */
	public static final String ACTION_TYPE_REPAIR = "修补";
	
	private Integer id;//主键
	private String tyre_id;//胎号
	private String tyre_action;//事件属性
	private String tyre_content;//内容,描述
	private String tyre_person;//人
	private Date tyre_time;//时间
	private Integer user_id;//用户id
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTyre_id() {
		return tyre_id;
	}
	public void setTyre_id(String tyre_id) {
		this.tyre_id = tyre_id;
	}
	public String getTyre_action() {
		return tyre_action;
	}
	public void setTyre_action(String tyre_action) {
		this.tyre_action = tyre_action;
	}
	public String getTyre_content() {
		return tyre_content;
	}
	public void setTyre_content(String tyre_content) {
		this.tyre_content = tyre_content;
	}
	public String getTyre_person() {
		return tyre_person;
	}
	public void setTyre_person(String tyre_person) {
		this.tyre_person = tyre_person;
	}
	public Date getTyre_time() {
		return tyre_time;
	}
	public void setTyre_time(Date tyre_time) {
		this.tyre_time = tyre_time;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	
	
}
