package com.psylife.util.push;

import java.util.Date;

public class Message {
	private String dtu_id;

	private String warn_msg;

	private Integer senduserId;

	private Integer userId;

	private Integer status;

	private Date createTime;

	private String trucks_id;

	private String title;
	
	private String company;
	
	private int msg_id;

	public Message() {
	}

	public Message(String dtu_id, String warn_msg, Integer senduserId, Integer userId, Integer status, Date createTime,
			String trucks_id, String title,String company,int msg_id) {
		super();
		this.dtu_id = dtu_id;
		this.warn_msg = warn_msg;
		this.senduserId = senduserId;
		this.userId = userId;
		this.status = status;
		this.createTime = createTime;
		this.trucks_id = trucks_id;
		this.title = title;
		this.company=company;
		this.msg_id=msg_id;
	}


	public Integer getSenduserId() {
		return senduserId;
	}

	public void setSenduserId(Integer senduserId) {
		this.senduserId = senduserId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}


	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}


	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public int getMsg_id() {
		return msg_id;
	}

	public void setMsg_id(int msg_id) {
		this.msg_id = msg_id;
	}

	public String getDtu_id() {
		return dtu_id;
	}

	public void setDtu_id(String dtu_id) {
		this.dtu_id = dtu_id;
	}

	public String getWarn_msg() {
		return warn_msg;
	}

	public void setWarn_msg(String warn_msg) {
		this.warn_msg = warn_msg;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getTrucks_id() {
		return trucks_id;
	}

	public void setTrucks_id(String trucks_id) {
		this.trucks_id = trucks_id;
	}
	
}