package com.psylife.vo;

import java.util.Date;

public class TreadPattern {

	private Integer id;

	private String deviceId;
	// 车牌号
	private String carNo;
	// 车胎位置
	private String tyrePosition;
	// 单耗磨损值
	private float abrasionValue;
	// 异常损伤类型
	private Integer injuryType;
	// 轮胎深度值
	private Float tyreDeep;

	private Date createdTime = new Date();

	public TreadPattern() {
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getCarNo() {
		return carNo;
	}

	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}

	public String getTyrePosition() {
		return tyrePosition;
	}

	public void setTyrePosition(String tyrePosition) {
		this.tyrePosition = tyrePosition;
	}

	public float getAbrasionValue() {
		return abrasionValue;
	}

	public void setAbrasionValue(float abrasionValue) {
		this.abrasionValue = abrasionValue;
	}

	public Integer getInjuryType() {
		return injuryType;
	}

	public void setInjuryType(Integer injuryType) {
		this.injuryType = injuryType;
	}

	public Float getTyreDeep() {
		return tyreDeep;
	}

	public void setTyreDeep(Float tyreDeep) {
		this.tyreDeep = tyreDeep;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	@Override
	public String toString() {
		return " [deviceId=" + deviceId + ", carNo=" + carNo + ", tyrePosition=" + tyrePosition + ", abrasionValue="
				+ abrasionValue + ", injuryType=" + injuryType + ", tyreDeep=" + tyreDeep + "]";
	}

}
