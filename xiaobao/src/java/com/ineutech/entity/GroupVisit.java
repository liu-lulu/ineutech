package com.ineutech.entity;

public class GroupVisit {
	private Integer group_id;
	private String purpose;
	private Integer purpose_count;
	public Integer getGroup_id() {
		return group_id;
	}
	public void setGroup_id(Integer group_id) {
		this.group_id = group_id;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public Integer getPurpose_count() {
		return purpose_count;
	}
	public void setPurpose_count(Integer purpose_count) {
		this.purpose_count = purpose_count;
	}
	

}
