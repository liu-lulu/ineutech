package com.kkbc.entity;

public class CompanyInfo {
	private String company_name;
	private String member_s_count;
	private String company_s_rank;
	private Integer shixin;
	
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getMember_s_count() {
		return member_s_count;
	}
	public void setMember_s_count(String member_s_count) {
		this.member_s_count = member_s_count;
	}
	public String getCompany_s_rank() {
		return company_s_rank;
	}
	public void setCompany_s_rank(String company_s_rank) {
		this.company_s_rank = company_s_rank;
	}
	public Integer getShixin() {
		return shixin;
	}
	public void setShixin(Integer shixin) {
		this.shixin = shixin;
	}
	

}
