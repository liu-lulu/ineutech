package com.kkbc.vo;

public class Education {
	private String schoolLocation;
	private String schoolCountry;
	private String schoolName;
	private String schoolNameOther;
	private String degree;
	private String dateOfAward;
	private String educationLevel;
	private String modeOfStudy;
	private String major;
	private String firstLevelDiscipline;
	private String gpa;
	public String getSchoolLocation() {
		return schoolLocation;
	}
	public void setSchoolLocation(String schoolLocation) {
		this.schoolLocation = schoolLocation;
	}
	public String getSchoolCountry() {
		return schoolCountry;
	}
	public void setSchoolCountry(String schoolCountry) {
		this.schoolCountry = schoolCountry;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolNameOther() {
		return schoolNameOther;
	}
	public void setSchoolNameOther(String schoolNameOther) {
		this.schoolNameOther = schoolNameOther;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getDateOfAward() {
		return dateOfAward;
	}
	public void setDateOfAward(String dateOfAward) {
		this.dateOfAward = dateOfAward;
	}
	public String getEducationLevel() {
		return educationLevel;
	}
	public void setEducationLevel(String educationLevel) {
		this.educationLevel = educationLevel;
	}
	public String getModeOfStudy() {
		return modeOfStudy;
	}
	public void setModeOfStudy(String modeOfStudy) {
		this.modeOfStudy = modeOfStudy;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getFirstLevelDiscipline() {
		return firstLevelDiscipline;
	}
	public void setFirstLevelDiscipline(String firstLevelDiscipline) {
		this.firstLevelDiscipline = firstLevelDiscipline;
	}
	public String getGpa() {
		return gpa;
	}
	public void setGpa(String gpa) {
		this.gpa = gpa;
	}
	
	
}
