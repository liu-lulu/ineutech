package com.kkbc.vo;

public class HonorAward {
	
	private String haDateofAward;
	private String haCertificationName;
	private String haAccreditingOrganization;
	private String haHonorsLevel;
	public String getHaDateofAward() {
		return haDateofAward;
	}
	public void setHaDateofAward(String haDateofAward) {
		this.haDateofAward = haDateofAward;
	}
	public String getHaCertificationName() {
		return haCertificationName;
	}
	public void setHaCertificationName(String haCertificationName) {
		this.haCertificationName = haCertificationName;
	}
	public String getHaAccreditingOrganization() {
		return haAccreditingOrganization;
	}
	public void setHaAccreditingOrganization(String haAccreditingOrganization) {
		this.haAccreditingOrganization = haAccreditingOrganization;
	}
	public String getHaHonorsLevel() {
		return haHonorsLevel;
	}
	public void setHaHonorsLevel(String haHonorsLevel) {
		this.haHonorsLevel = haHonorsLevel;
	}

}
