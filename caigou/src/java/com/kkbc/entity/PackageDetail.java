package com.kkbc.entity;

import java.util.Date;

public class PackageDetail {
	private Integer packageDetail_id;
	private Integer package_id;
	private Integer order_id;
	private Integer order_detail_id;
	private Date create_time;
	private String brand;
	private String model;
	private Integer count;
	private Float purchase_price;
	
	public PackageDetail(){}
	public PackageDetail(Integer package_id,Integer order_id,Integer order_detail_id,String brand,String model,Integer count,Float purchase_price){
		this.package_id=package_id;
		this.order_id=order_id;
		this.order_detail_id=order_detail_id;
		this.brand=brand;
		this.model=model;
		this.count=count;
		this.purchase_price=purchase_price;
	}
	public Integer getPackageDetail_id() {
		return packageDetail_id;
	}
	public void setPackageDetail_id(Integer packageDetail_id) {
		this.packageDetail_id = packageDetail_id;
	}
	public Integer getPackage_id() {
		return package_id;
	}
	public void setPackage_id(Integer package_id) {
		this.package_id = package_id;
	}
	public Integer getOrder_id() {
		return order_id;
	}
	public void setOrder_id(Integer order_id) {
		this.order_id = order_id;
	}
	public Integer getOrder_detail_id() {
		return order_detail_id;
	}
	public void setOrder_detail_id(Integer order_detail_id) {
		this.order_detail_id = order_detail_id;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Float getPurchase_price() {
		return purchase_price;
	}
	public void setPurchase_price(Float purchase_price) {
		this.purchase_price = purchase_price;
	}
	public Date getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

}
